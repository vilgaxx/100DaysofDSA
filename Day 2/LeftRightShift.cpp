#include<iostream>
using namespace std;

int main( ) {
    int a = 4;
    int b = 6;
    // left shift 
    cout << (a << 1) <<endl ;
    cout << (b << 1) <<endl ;

    // it acts like multiplying by 2 in this case

    // right shift 

    cout << (a >> 1) <<endl;
    cout << (b >> 1) <<endl;

    // it acts like divide by 2 in this case

}